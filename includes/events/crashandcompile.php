<h2>.:crash and compile:.</h2>
<h3>.:what?:.</h3>
<p>
    You may have seen this at DefCon. Crash and Compile is programming contest that involves booze. It is an ACM-style programming contest meshed together with a drinking game. 
    <br />
    There will be teams of 1-2 people show up with their dev environment of choice, grab a drink and a programming problem, and start coding.
    <br />
</p>
<h3>.:how do you play:.</h3>
<div class="boxedContent">
<ul>

<li>If your code fails to compile, you take a drink.</li>
<li>If your code compiles, but fails to run, you take a drink.</li>
<li>If your code runs, but fails to produce the correct output, you take a drink.</li>
<li>The first team to successfully produce the right output gets 3 points. Everyone still working drinks.</li>
<li>The second team to successfully produce the right output gets 2 points. Everyone still working drinks.</li>
<li>The third team to successfully produce the right output gets 1 point. Everyone still working drinks.</li>
<li>The winner is the team who is the most smashed at the end of the night. We might also hand out a few prizes to the nerds who can actually write code too.</li>

<li>Programs can be written in any language and platform that can produce the appropriate output (usually just text.) I've seen people WIN using nothing but a compiler on old school Palm Pilot, writing code in Graffiti.</li>
</ul>
</div>
<h3>.:about the teams:.</h3>
<p>
In addition to the competing teams describe above, there are two more teams:
<br /><br />
Team Control Group: If you're a Teetotaler like me, or somehow got roped into being a designated driver for the evening, you can still participate. TCG doesn't actually compete for points (or drinks obviously), but if any competing team completes a problem before TCG, then everyone still working takes a drink. Basically, if a team can program better drunk than TCG can sober, that is deserving of a drink for everyone else.
<br /><br />
Team Distraction: Don't feel like writing code? Do you just want to hang out and watch your friends (or complete strangers) try to program and drink at the same time? Do you want to make their lives even more difficult? Then we invite you to join Team Distraction! TD can have as many members as we can fit into the room, so don't be shy.
</p>
