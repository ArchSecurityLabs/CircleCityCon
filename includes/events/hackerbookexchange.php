<h2>.:hacker book exchange:.</h2>
    
<h3>.:what the heck is this?:.</h3>
<p>
    It is exactly like what it says, a book exchange, but hacker edition.    
    The idea behind it is that everyone has at least one computer/security book that they have not looked at in a long time. A book they could part with that someone else might find value in. 
</p>
<h3>.:what is the purpose?:.</h3>
<p>
    The purpose of this event is to have a free way for people to explore new books and ideas. 
</p>
<h3>.:who can participate?:.</h3>
<p>
    This event is open to anyone, all we ask is that you bring AT LEAST one book you think someone might find value, or knowledge in. We do not guarantee everyone amazing books, but we hope so, which is why we ask you to bring at least one book. 
</p>
<h3>.:questions?:.</h3>
<p>If you have any questions about this event feel free to reach out to us.</p>
