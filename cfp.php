<h2>.:Submission Guidelines:.</h2>
<p>
We are trying out the double-blind method of conference reviewing. This means we will not see author names when reviewing talks and authors will not know the exact reviewers that judged their submission. We hope this will provide a low level of bias when judging talks. This means that your abstract and talk outline shouldn't include your name or any information that might allow us to identify you while reviewing your talk. A list of what we expect each submission to include is below. 
We are requesting the following items for each submission:
</p>

                <div class="boxedContent">
                    <ol>
                        <li>A submission title as it will appear in the conference program.</li>
                        <li>Notification if you are a full-time student at a University. We will be requesting proof of this later if your presentation is accepted.</li>
                        <li>Author Information (this is how we'll contact you). <b>Reviewers will not see this information </b>.</li>
                        <li>The topic that best meets your talk.</li>
                        <li>An abstract for your talk and any keywords you think will help us get a better idea of what to expect. (200 words or less)</li>
                        <li>A plain text file with a detailed outline of your talk. This is what you will upload.</li>
                        <li>Your short author bio (as it should appear in the conference program) and whether or not this talk has been given before at other conference as well as what conference it was given at. This will go in the "Optional Comments" field. <b>Reviewers will not see this information</b>.</li>
                    </ol>
                </div>
    <p>
        If the submission is missing any of the above items, it will be thrown out. We reserve the right to throw out papers where author's personal information is contained in the abstract or plain text file. 

            <br />    
            Remember, the double-blinding is for your sake, not ours.

            Authors are in charge of any equipment beyond the Internet access and A/V equipment we already provide.

        <b>If a talk is given by multiple presenters we reserve the right to limit the number of free passes provided.</b>
    </p>
    
