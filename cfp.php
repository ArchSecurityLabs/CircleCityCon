<h2> Topics and Format: </h2>
    <p>
        All talks (except lightning talks) should be roughly 40-45 minutes with 5-10 minutes for questions.

        Lightning talks should be between 20-25 minutes with 5-10 minutes for questions. 

        We are looking for talks in the following areas (the examples provided with each topic are by no means a comprehensive list):
    </p>
        <div class="boxedContent">
            <br />
            <br />
            <b>Offense (Red Team)</b>  -- Offensive based talks involving malware, exploits, vulnerabilities, social engineering, and anything related to attacks and offense.
            <br />
            <br />
            <b>Defense (Blue Team)</b> -- System hardening, network protection, honeypots, user education to prevent social engineering, or anything else related to defense. 
            <br />
            <br />
            <b>Hybrid Offense/Defense</b> -- A talk combining topics that fall under both offense and defense.
            <br />
            <br />
            <b>Education</b> -- Anything that involves teaching the audience how to do something new in 50 minutes or less. Perhaps a tutorial on X86 assembly, writing your first exploit, or picking your first lock. The goal is a talk that can transform an audience that knows nothing or very little about a talk into an audience that knows enough to start exploring the area in their spare time. 
            <br />
            <br />
            <b>Lightning Talk (20-30 min)</b> -- Short talks that take in to account any of the above topics.
